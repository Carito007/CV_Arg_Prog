let mostrarTextouno = document.getElementById('titulo1');
let textouno = document.getElementById('infouno');

mostrarTextouno.onclick = mostraruno


function mostraruno() {
  if (textouno.style.display== "inline-block") {
    textouno.style.display = "none";
  } else {
    textouno.style.display = "inline-block";
  }
};

